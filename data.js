export const getWorkoutsByCategory = (category) => {
  if (category === 'All') {
    return workoutsData;
  }
  return workoutsData.filter(workout => workout.category === category);
};

export const searchWorkouts = (query, category = 'All') => {
  const filteredByCategory = getWorkoutsByCategory(category);
  if (!query.trim()) {
    return filteredByCategory;
  }
  
  return filteredByCategory.filter(workout => 
    workout.title.toLowerCase().includes(query.toLowerCase()) ||
    workout.description.toLowerCase().includes(query.toLowerCase()) ||
    workout.instructor.toLowerCase().includes(query.toLowerCase())
  );
};

export const getWorkoutById = (id) => {
  return workoutsData.find(workout => workout.id === id);
};

